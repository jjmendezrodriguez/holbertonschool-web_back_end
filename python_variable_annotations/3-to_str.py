#!/usr/bin/env python3
"""
This module provides a function to convert a floating-point
number to its string representation.
The function returns the number as a string.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the given float.
    """
    return str(n)
